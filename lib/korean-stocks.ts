export function resolveKoreanStockName(input: string): string {
  const key = input.trim().toLowerCase()
  const symbol = koreanStockMap[key]
  if (!symbol) return input
  // 숫자만 → KRX 종목 (.KS), 알파벳 → 미국 티커
  return /^\d+$/.test(symbol) ? `${symbol}.KS` : symbol
}

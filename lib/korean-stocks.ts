/**
 * Convert Korean stock name to symbol
 * Returns the symbol if found, otherwise returns the input as-is
 */
export function resolveKoreanStockName(input: string): string {
  const normalized = input.trim().toLowerCase()
  const symbol = koreanStockMap[normalized]
  return symbol ? `${symbol}.KS` : input
}
